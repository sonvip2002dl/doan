void USART3_IRQHandler(void)                	//串口2中断服务程序
{
	static u8 i=0;
	
	
	if(USART_GetITStatus(USART3, USART_IT_RXNE))  //接收中断(接收到的数据必须是0x0d 0x0a结尾)
	{	
		Res =USART_ReceiveData(USART3);//(USART2->DR);	//读取接收到的数据
		
		if(Res==0xEE)
		{
			flag=0;
			i=0;
			if(a[1]>128) pitch=a[1]-256;
			else pitch=a[1];
				
			if(a[2]>128) roll=a[2]-256;
			else roll=a[2];
				
			if(a[3]>128) yaw=a[3]-256;
			else yaw=a[3];
				
			//USART_SendData(USART3,yaw);
			PD();
		}
		
		if(flag==1)
		{
		  a[i++]=Res;
		}
		
		if(Res==0xAA)
		{
			flag=1;
		}
	}
} 
